#Эта програма придумывает оригинальное название блюду из ваших любимых блюд
print('Эта програма придумывает оригинальное название блюду из ваших любимых блюд')
bludo_1 = input("Напишите сюда ваще любимое блюдо\n")
bludo_2 = input("Напишите ещё одно блюдо\n")
bludo = bludo_1 + bludo_2
print("В результате мы получаем вот такое блюдо", bludo.title())